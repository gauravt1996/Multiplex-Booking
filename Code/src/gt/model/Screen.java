package gt.model;

public class Screen {
	
	private String thid;
	private String scid;
	private String sccap;
	private String scimg;
	private String scdesc;
	public String getThid() {
		return thid;
	}
	public void setThid(String thid) {
		this.thid = thid;
	}
	public String getScid() {
		return scid;
	}
	public void setScid(String scid) {
		this.scid = scid;
	}
	public String getSccap() {
		return sccap;
	}
	public void setSccap(String sccap) {
		this.sccap = sccap;
	}
	public String getScimg() {
		return scimg;
	}
	public void setScimg(String scimg) {
		this.scimg = scimg;
	}
	public String getScdesc() {
		return scdesc;
	}
	public void setScdesc(String scdesc) {
		this.scdesc = scdesc;
	}

}
